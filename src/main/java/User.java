public class User {
    private String _username;
    private String _password;
    private String _email;
    private String _phone_number;

    String getUserName() {
        return _username;
    }
    String getPassword() {
        return _password;
    }
    String getEmail() {
        return _email;
    }
    String getPhoneNumber() {
        return _phone_number;
    }
}
